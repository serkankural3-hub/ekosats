    if (productName == null) return 0.0;
    
    // Eğer ürün adı " - " içeriyorsa (ürün - renk formatı), sadece ürün adını al
    final cleanName = productName.contains(' - ') 
        ? productName.split(' - ').first.trim()
        : productName;
    
    return productWeights[cleanName] ?? 0.0;
  }

  static double totalWeight(String? productName, int quantity) {
    return weightFor(productName) * quantity;
